package delano.cursojava.aula11;

public class Sequenciaescape {

	public static void main(String[] args) {
		
		//"Hello, World!"
		System.out.print("\"Hello, World!\"\n\r");
		
		// 1\4
		
		System.out.println("1\\4");
		
		// \n = pula para proxima linha (nova linha)
		// \r = retorno do resultado (retorno de carro)
	}

}
